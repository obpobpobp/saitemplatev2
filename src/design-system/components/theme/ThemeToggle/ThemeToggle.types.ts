/**
 * Props for ThemeToggle component
 */
export interface ThemeToggleProps {
  /**
   * Optional custom class name
   */
  className?: string;
  
  /**
   * Size variant
   * @default 'medium'
   */
  size?: 'small' | 'medium' | 'large';
  
  /**
   * Show label text next to icon
   * @default false
   */
  showLabel?: boolean;
}









